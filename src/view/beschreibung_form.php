<h2>Schlosslauf</h2>
<p>
    Der Schlosslauf ist ein traditionsreicher Lauf in Neuenburg. Jährlich versammeln sich am ersten Tag des neuen Jahres hunderte Laufbegeisterte, um am Schlosslauf teilzunehmen.
</p>
<p>
    Die verschiedenen Routen sind unterschiedlich lang, so dass jeder etwas passendes findet, und führen alle an mindestens zwei Schlössern/Burgen vorbei.
</p>
<p>
    Anfangs- und Endpunkt sind im Herzen der Altstadt von Neuenburg, wo sich auch viele Zuschauer versammeln. Dort stellen auch jedes Jahr verschiedenste Essenstände ihre Zelte auf, damit die Verpflegung nicht zu kurz kommt.
</p></br>
